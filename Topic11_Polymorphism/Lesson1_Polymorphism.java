package Topic11_Polymorphism;

// Polymorphism - Полиморфизъм
// Полиморфизмът в Java е задачата, която изпълнява едно действие
// по различни начини.
// Poly - many (много) 
// morphism - behaviour (поведение)
// Един обект може да има множестово разплични поведения в зависимост
// от това как ще създадем инстанцията му.

// Има два вида полиморфизъм:
// 1. compile-time polymorphism - ако още преди изпълнението знаем 
//    какво ще се изведе (Overloading)
//    - пример: метода със същия име, различен брой параметри
//      add(), add(int a, int b), add(double a, double b)
// 2. runtime polymorphism - ако по време на изпълнението се решава
//    какво ще се изведе (Oveerriding)
//    - пример: два класа A и B. Клас В наследява клас А. И класа родител и 
//      подкласа имат метод add(int a, int b). Кой от двата метода да се изпълни
//      ще се разбере по време на изпълнението(run-time).



